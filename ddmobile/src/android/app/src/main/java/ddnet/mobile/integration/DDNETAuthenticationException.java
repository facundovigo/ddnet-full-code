package ddnet.mobile.integration;

public class DDNETAuthenticationException extends Exception {
    public DDNETAuthenticationException() {
        super();
    }
    public DDNETAuthenticationException(String message) {
        super(message);
    }
    public DDNETAuthenticationException(Throwable cause) {
        super(cause);
    }
    public DDNETAuthenticationException(String message, Throwable cause) {
        super(message, cause);
    }
}
